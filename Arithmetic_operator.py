#variable x holds 6 and variable y holds 3

x = 6
y = 3

#Addition
print ("Addition, x(6) + y(3) =" , x + y)

#subtraction
print ("subtraction, x(6) - y(3) =" , x - y)

#multiplication
print ("multiplication, x(6) * y(3) =" , x * y)

#Division
print ("Division, x(6) * y(3) =", x / y )

#Modulus
print ("Modulus, x(6) % y(3) =" , x % y)

#exponent
print ("exponent, x(6) ** y(3) =" , x ** y)

#integer division rounded towards minus infinity
print ("floor division, x(6) // y(3) =" , x // y)



